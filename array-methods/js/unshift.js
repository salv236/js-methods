// unshift method adds an entry at the beginning of the index

let animals = ['dog', 'cat', 'monkey', 'gerbil'];  

// guerilla = 0 dog = 1 car = 2 monkey = 3 gerbil = 4
animals.unshift('guerilla');

console.log(animals);

// add multiple entries at the beginning of the array

animals.unshift('horse', 'rabbit', 'pig');
// horse = 0, rabbit = 1, pig = 2, guerilla = 3 dog = 4 car = 5 monkey = 6 gerbil = 7 
console.log(animals)



